 CREATE TABLE BROADCASTING_STATION (
	BNO				INT			NOT NULL,
	BROADNAME		CHAR(20)	NOT NULL,
	LOCATION		CHAR(100)	NOT NULL,
	PRIMARY KEY(BNO)
);

CREATE TABLE TVPROGRAM (
		TVPNO		INT			NOT NULL,
		TVPNAME		CHAR(50)	NOT NULL,
		RATING		float,
		TVPTIME		CHAR(100),
		F_BNO_TV	INT,
		
		PRIMARY KEY(TVPNO),
		FOREIGN KEY(F_BNO_TV) REFERENCES BROADCASTING_STATION(BNO) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE ENTERTAINMENT (
		ENTNO		INT			NOT NULL,
		ENTNAME		CHAR(20)	NOT NULL,
		LOCATION	CHAR(100)	NOT NULL,

		PRIMARY KEY(ENTNO)
);

CREATE TABLE CAST_ (
		CASTNO		INT			NOT NULL,
		CASTJOB		CHAR(20)	NOT NULL,
		CASTNAME	CHAR(20)	NOT NULL,
		CASTAGE		INT			NOT NULL,
		F_ENTNO		INT,
		F_TVPNO		INT,

		PRIMARY KEY(CASTNO),
		FOREIGN KEY(F_ENTNO) REFERENCES ENTERTAINMENT(ENTNO) ON DELETE SET NULL ON UPDATE CASCADE,
		FOREIGN KEY(F_TVPNO) REFERENCES TVPROGRAM(TVPNO) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE EMPLOYEE (
		EMPNO				INT				NOT NULL,
		EMPNAME				CHAR(20)		NOT NULL,
		SALARY				INT				NOT NULL,
		F_BNO_EMP			INT				NOT NULL,					

		PRIMARY KEY(EMPNO),
		FOREIGN KEY(F_BNO_EMP) REFERENCES BROADCASTING_STATION(BNO) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE DEPENDENT_ (
		DEPNAME					CHAR(12)		NOT NULL,
		WEDDINGANNVIERSARY		CHAR(6)			NOT NULL,
		F_EMPNO					INT				NOT NULL,


		PRIMARY KEY(DEPNAME, F_EMPNO),
		FOREIGN KEY(F_EMPNO) REFERENCES EMPLOYEE(EMPNO) ON DELETE CASCADE ON UPDATE CASCADE
);	

